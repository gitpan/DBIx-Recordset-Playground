#!/usr/bin/perl

#=====================================================================
# DECLARATIONS
#=====================================================================
use strict;
use Whitespace;

#=====================================================================
#  PROGRAM PROPER
#=====================================================================

my @file = <*.pl>;


for my $file ( @file ) {


    my $ws = new Whitespace($file, "$file-ws");
    $ws->trailclean;
    rename ("$file-ws", $file);

}

#=====================================================================
#  SUBROUTINES
#=====================================================================

1; 

